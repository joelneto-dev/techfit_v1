    // --- Lógica Navbar (Recolher perto do CTA) ---
    document.addEventListener('DOMContentLoaded', function() {
        const header = document.getElementById('main-header');
        let isHidden = false;

        function checkNavbar() {
            const ctaSections = document.querySelectorAll('section.bg-\\[\\#f4f4f4\\]');
            let ctaSection = null;
            
            ctaSections.forEach(section => {
                const h2 = section.querySelector('h2');
                if (h2 && h2.textContent.includes('Comece sua transformação')) {
                    ctaSection = section;
                }
            });

            if (!ctaSection) return;

            const ctaRect = ctaSection.getBoundingClientRect();
            const headerHeight = header.offsetHeight;
            const distanceFromTop = ctaRect.top;

            if (distanceFromTop <= headerHeight + 30 && !isHidden) {
                header.style.transform = 'translateY(-100%)';
                header.style.transition = 'transform 0.4s cubic-bezier(0.4, 0, 0.2, 1)';
                isHidden = true;
            } 
            else if (distanceFromTop > headerHeight + 50 && isHidden) {
                header.style.transform = 'translateY(0)';
                isHidden = false;
            }
        }

        window.addEventListener('scroll', checkNavbar, { passive: true });
        checkNavbar();
    });
