    <script>
    document.addEventListener('DOMContentLoaded', function() {
        const textPhrases = ['na palma da mão', 'na ponta dos dedos', 'no seu pulso'];
        const iconNames = ['smartphone', 'fingerprint', 'watch'];

        const textEl = document.getElementById('dynamic-text');
        const iconEl = document.getElementById('dynamic-icon');
        const cursorEl = document.getElementById('cursor');
        if (!textEl || !iconEl || !cursorEl) return;

        let idx = 0;
        let charIdx = 0;
        let isDeleting = false;

        function renderIconsIfAvailable() {
            try {
                if (window.lucide && typeof lucide.createIcons === 'function') {
                    lucide.createIcons();
                }
            } catch (e) {}
        }

        function updateIcon() {
            iconEl.style.opacity = '0';
            iconEl.style.transform = 'rotateY(90deg)';
            setTimeout(() => {
                iconEl.innerHTML = `<i data-lucide="${iconNames[idx]}" class="w-8 h-8"></i>`;
                renderIconsIfAvailable();
                iconEl.style.opacity = '1';
                iconEl.style.transform = 'rotateY(0deg)';
            }, 250);
        }

        function typeText() {
            const currentText = textPhrases[idx];
            const typingSpeed = 50;
            const deletingSpeed = 30;
            const delayBetweenPhrases = 2000;

            if (!isDeleting) {
                if (charIdx < currentText.length) {
                    textEl.innerHTML = currentText.substring(0, charIdx + 1);
                    charIdx++;
                    setTimeout(typeText, typingSpeed);
                } else {
                    setTimeout(() => {
                        isDeleting = true;
                        typeText();
                    }, delayBetweenPhrases);
                }
            } else {
                if (charIdx > 0) {
                    textEl.innerHTML = currentText.substring(0, charIdx - 1);
                    charIdx--;
                    setTimeout(typeText, deletingSpeed);
                } else {
                    isDeleting = false;
                    idx = (idx + 1) % textPhrases.length;
                    charIdx = 0;
                    updateIcon();
                    setTimeout(typeText, 500);
                }
            }
        }

        updateIcon();
        typeText();
    });
    </script>
    <script>
    document.addEventListener('DOMContentLoaded', function() {
        const video = document.getElementById('app-video');
        const btn = document.getElementById('video-toggle-btn');
        if (!video || !btn) return;

        let lastPausedState = null;

        function updateIcon() {
            const isPaused = video.paused;
            if (lastPausedState === isPaused) return;
            lastPausedState = isPaused;

            try {
                btn.innerHTML = '';
                const iconEl = document.createElement('i');
                iconEl.setAttribute('data-lucide', isPaused ? 'play' : 'pause');
                iconEl.className = 'w-5 h-5';
                btn.appendChild(iconEl);
                btn.setAttribute('aria-label', isPaused ? 'Reproduzir vídeo' : 'Pausar vídeo');

                if (window.lucide && window.lucide.createIcons) {
                    window.lucide.createIcons();
                }
            } catch (e) {}
        }

        btn.addEventListener('click', function(e) {
            e.preventDefault();
            e.stopPropagation();
            if (video.paused) {
                video.play().catch(err => console.error(err));
            } else {
                video.pause();
            }
            setTimeout(updateIcon, 100);
        });

        setInterval(updateIcon, 500);
        updateIcon();
    });
    </script>
